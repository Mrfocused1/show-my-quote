import { setCors, requireApiKey } from './_lib/auth.js';
import { getSupabase } from './_lib/supabase.js';

export default async function handler(req, res) {
  setCors(req, res);
  if (req.method === 'OPTIONS') return res.status(200).end();

  const projectId = process.env.SIGNALWIRE_PROJECT_ID;
  const apiToken  = process.env.SIGNALWIRE_API_TOKEN;
  const spaceUrl  = process.env.SIGNALWIRE_SPACE_URL;

  // POST — recording status callback webhook from SignalWire (no auth check needed here)
  if (req.method === 'POST') {
    const { RecordingSid, RecordingStatus, CallSid } = req.body || {};
    console.log('Recording callback:', { RecordingSid, RecordingStatus, CallSid });
    if (RecordingStatus === 'completed' && RecordingSid && CallSid) {
      const supabase = getSupabase();
      if (supabase) {
        await supabase.from('calls').update({ recording_sid: RecordingSid }).eq('call_sid', CallSid);
      }
    }
    return res.status(200).end();
  }

  const callSid      = req.query?.callSid;
  const stream       = req.query?.stream;
  const recordingSid = req.query?.recordingSid;

  // Stream mode is called via <audio src=...> which can't send headers — skip auth
  // (recordingSid is a UUID generated by SignalWire, not guessable)
  if (!(stream === 'true' && recordingSid)) {
    // GET metadata — require API key
    if (!requireApiKey(req, res)) return;
  }

  if (!callSid && !recordingSid) {
    return res.status(400).json({ error: 'missing callSid or recordingSid' });
  }

  if (!projectId || !apiToken || !spaceUrl) {
    return res.status(503).json({ error: 'SignalWire not configured' });
  }

  const authHeader = 'Basic ' + Buffer.from(`${projectId}:${apiToken}`).toString('base64');
  const baseUrl    = `https://${spaceUrl}/api/laml/2010-04-01/Accounts/${projectId}`;

  // Stream mode: proxy the MP3 audio from SignalWire
  if (stream === 'true' && recordingSid) {
    try {
      const mp3Url  = `${baseUrl}/Recordings/${recordingSid}.mp3`;
      const audioRes = await fetch(mp3Url, { headers: { Authorization: authHeader }, redirect: 'follow' });
      if (!audioRes.ok) return res.status(502).json({ error: 'Failed to fetch audio' });
      res.setHeader('Content-Type', 'audio/mpeg');
      res.setHeader('Cache-Control', 'public, max-age=3600');
      return res.send(Buffer.from(await audioRes.arrayBuffer()));
    } catch (err) {
      console.error('Audio proxy error:', err);
      return res.status(500).json({ error: 'Internal error' });
    }
  }

  // Metadata mode: check if a completed recording exists for this CallSid
  try {
    const url      = `${baseUrl}/Calls/${callSid}/Recordings.json`;
    const response = await fetch(url, { headers: { Authorization: authHeader } });
    if (!response.ok) {
      const err = await response.text();
      console.error('SignalWire recordings fetch failed:', err);
      return res.status(502).json({ error: 'Failed to fetch recordings' });
    }
    const data       = await response.json();
    const recordings = data.recordings || [];
    const completed  = recordings.find(r => r.status === 'completed');
    if (!completed) return res.json({ ready: false });
    return res.json({ ready: true, recordingSid: completed.sid, duration: completed.duration });
  } catch (err) {
    console.error('Error fetching recording:', err);
    return res.status(500).json({ error: 'Internal error' });
  }
}
